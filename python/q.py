x1 = int(input("Введите номер столбца первой клетки: "))
y1 = int(input("Введите номер строки первой клетки: "))
x2 = int(input("Введите номер столбца второй клетки: "))
y2 = int(input("Введите номер строки второй клетки: "))

# Проверяем, находятся ли координаты в пределах доски 8x8
if x1 < 1 or x1 > 8 or y1 < 1 or y1 > 8 or x2 < 1 or x2 > 8 or y2 < 1 or y2 > 8:
    print("Ошибка!")
else:
    dx = x1 - x2  # Разница по горизонтали
    dy = y1 - y2  # Разница по вертикали

    # Проверяем движение коня (буквой "Г")
    if (dx == 2 or dx == -2) and (dy == 1 or dy == -1):
        print("YES")
    elif (dx == 1 or dx == -1) and (dy == 2 or dy == -2):
        print("YES")
    else:
        print("NO")
